// scale a coords string "x1,y1,x2,y2,..." to new width/height
export function scaleCoords(
    coords: string,
    mods: MapMods,
    newWidth: number,
    newHeight: number
): string {
    const sx = newWidth / mods.originalWidth;
    const sy = newHeight /mods.originalHeight;

    const nums = coords
        .split(/[,\s]+/)
        .filter(Boolean)
        .map((n) => Number(n));

    for (let i = 0; i < nums.length; i += 2) {
        nums[i] = Math.round(nums[i] * sx) + mods.offsetX; // x
        nums[i + 1] = Math.round(nums[i + 1] * sy) + mods.offsetY; // y
    }

    return nums.join(',');
}

